<div class="staff_bio_right">
<p>Called to the Ontario Bar, 1974<br />Admitted to the Bar for the State of New York, 1988</p>
<p>Dennis Brans practices primarily in the hospitality and leisure area, specializing in business and real estate law.  As head of the hospitality and leisure group at Baldwin Anka Sennecke Halman LLP, Dennis has extensive experience in the acquisition, sale, development and financing of hotels, resorts, condominiums, time-share projects and investment properties. His involvement in the hospitality industry in Canada, the United States and overseas includes the negotiation and drafting of management and franchise agreements, acting both for owners as well as managers and franchisors.</p>
<p>Dennis has acted for both public and private companies, handling complicated purchase and sale transactions.  Some recent transactions involved:</p>
<ul>
<li>the acquisition on behalf of a Canadian public entity of a portfolio of three hotels totaling $52 million</li>
<li>the acquisition on behalf of a Canadian public entity of a portfolio of seven hotels totaling $56.8 million</li>
<li>the administration and supervision of the acquisition of eighteen hotel properties throughout Canada by a Canadian public entity for a total of $118 million</li>
<li>the acquisition from primarily individual vendors of twenty two hotel properties by a Canadian private entity and their refinancing for a total of $150 million</li>
<li>the sale to a United States public entity of a portfolio of twenty one hotel properties for a total of $US73.4 million</li>
</ul>
<p>Dennis is also legal counsel to a number of international clients, particularly from the Far East and Japan, whom he advises in connection with their Canadian and American real estate investments and business affairs.</p>
